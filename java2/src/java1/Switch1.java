package java1;

public class Switch1 {

	public static void main(String[] args) {
		int a = 10;
		switch(a) {
		case 1 :
		case 10 : 
			System.out.println("옵션1");
		break;
		case 2 : System.out.println("옵션2");
		break;
		case 3 : System.out.println("옵션3");
		break;
		default : System.out.println("옵션값이 잘못 되었습니다.");
		break;
		
		}

	}

}
